CREATE PROCEDURE public.process_cart_payment(IN p_cart_id bigint)
    LANGUAGE plpgsql
    AS $$
BEGIN
    UPDATE offer
    SET available_spots = available_spots - (
        SELECT COUNT(*)
        FROM traveler_details td
        INNER JOIN reservation r ON td.reservation_id = r.id
        WHERE r.cart_id = p_cart_id AND r.is_paid = FALSE AND r.offer_id = offer.id
    );
	
	UPDATE reservation r
    SET is_paid = TRUE
    WHERE cart_id = p_cart_id AND is_paid = FALSE;

    IF EXISTS (
        SELECT 1 FROM offer WHERE available_spots < 0
    ) THEN
        RAISE EXCEPTION 'Available spots are insufficient for one or more offers';
    END IF;

    UPDATE cart
    SET total_price = 0, number_of_items = 0
    WHERE id = p_cart_id;
END;
$$;


